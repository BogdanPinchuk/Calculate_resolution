function W_i = koordtoch(W, w, m, dw)
% Функція розрахунку кутових координат пікселів

% W_i - W - масив даних країв пікселів
% W - Wxi, Wyi - масив даних центрів пікселів
% w - wx, wy - кутова величина активної частини пікселя або всього піксеся
% m - mx, my - рівні розташування точок
% dw - dwx, dwy - різниця кутових координат пікселів

W_i = W + 0.5 .* m .* (w - m .* dw);

end
