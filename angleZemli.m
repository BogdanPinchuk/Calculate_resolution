function thetaZ = angleZemli(H, Rk, alpha)
% Функція розрахунку врутрішнього кута Землі
% та кута між площинами в точці надиру і апроксимованої в точці
% спостереження

% H - Реальна висота супутника в надирі
% Rk - радіус кривизни Землі у відповідній точці
% alpha кут відхилення КА від надиру

% Кут між точкою надиру і точкою на поверхні Землі
thetaZ = asin(sin(alpha) * (H + Rk) / Rk) - alpha;

end